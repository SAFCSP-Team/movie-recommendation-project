public class Movie {

    int id;
    String name;
    String type;
    int year;
    double rate;
    String generality;

    Movie(int id, String name, String type, int year, double d, String generality){
        this.id = id;
        this.name = name;
        this.type = type;
        this.year = year;
        this.rate = d;
        this.generality = generality;
    }
    
}
